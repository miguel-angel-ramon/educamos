package es.jccm.edu.proyectosfct.adapter.out.repositories;

import java.util.List;

import org.springframework.data.jpa.repository.Query;
import org.springframework.stereotype.Repository;
import es.jccm.edu.proyectosfct.application.domain.convenios.entities.ConvenioListFct;
import es.jccm.edu.proyectosfct.application.domain.convenios.entities.QConvenioListFct;
import es.jccm.edu.proyectosfct.application.domain.usuarios.projection.ParamsFCTProjection;
import es.jccm.edu.shared.adapter.out.repositories.AbstractRepository;

@Repository
public interface ParamsFCTRepository extends AbstractRepository<ConvenioListFct, Long, QConvenioListFct> {
 
    @Query(value = " SELECT * FROM ("
      + "   SELECT PTE.X_EMPLEADO AS x_empleado,             "
      + "                      TO_CHAR(PTE.F_TOMAPOS,'dd/MM/yyyy') AS f_tomapos,             "
      + "                   PTE.X_CENTRO AS x_centro,             "
      + "                  TLF_ANNOACTUAL AS c_anno,             "
      + "                   TO_CHAR(:xUsuario) AS x_usuario,                "
      + "                          CASE        "
      + "                          WHEN :vista = 'Direccion' THEN '161'          "
      + "               ELSE (select TO_CHAR(x_perfil) from tlperfiles where x_perfil = POP.x_perfil ) END x_perfil,       "
      + "               CASE        "
      + "               WHEN :vista = 'Direccion' THEN 'C'             "
      + "               ELSE (select c_codigo from tlperfiles where x_perfil = POP.x_perfil ) END codigo_perfil,       "
      + "               NULL sustituto,              "
      + "               NULL idtutorfctdual,              "
      + "               -1 idtutorfctdualsus,                     "
      + "               '1' s_modulo,              "
      + "               '1' x_ident,              "
      + "               CASE  "
      + "               WHEN :vista = 'Direccion'  AND (SELECT DISTINCT (1) FROM TLCARGOSEMP cem, tlcargos car  "
      + "                                               WHERE car.c_cargo = cem.c_cargo  "
      + "                                               AND cem.X_EMPLEADO =PTE.X_EMPLEADO " 
      + "                                               AND TLF_INTERSECPER(cem.F_TOMPOS,cem.F_CESE,SYSDATE,SYSDATE)=1 " 
      + "                                               AND cem.F_TOMAPOS = PTE.F_TOMAPOS       "
      + "                                               AND cem.c_cargo = 'XDI') = 1 THEN '1' "
      + "                ELSE '0' END esDirector, "      
      + "               CASE "
      + "               WHEN (SELECT count(distinct x_empleado) " 
      + "                     FROM  TLMIEDEPART dep  "
      + "                     WHERE dep.X_EMPLEADO = PTE.X_EMPLEADO " 
      + "                     AND dep.F_TOMAPOS = PTE.F_TOMAPOS  "
      + "                     AND dep.L_JEFE = 'S'  "
      + "                     AND (DEP.F_FIN > TRUNC(SYSDATE) OR DEP.F_FIN IS NULL)) >0 THEN '1' "
      + "               ELSE '0' END esJefeDep, "
      + "               CASE  "
      + "               WHEN  (SELECT COUNT(DISTINCT  1)  "
      + "                      FROM TLCARGOSEMP car " 
      + "                      WHERE car.X_EMPLEADO = PTE.X_EMPLEADO " 
      + "                      AND car.X_EMPLEADO IS NOT NULL  "
      + "                      AND car.F_TOMAPOS = PTE.F_TOMAPOS " 
      + "                      AND (CAR.F_CESE  > TRUNC(SYSDATE) OR CAR.F_CESE  IS NULL) " 
      + "                      AND car.C_CARGO = 'XCC') >0 THEN '1' "
      + "               ELSE '0' end esCoorCiclo, "
      + "              (select c_provincia from tldatoscen where x_centro = :idCentro and ROWNUM <= 1) c_provincia, " 
      + "                1 orden "
      + "          FROM TLPTOTRAEMP PTE, TLPUEORIPER POP       "
      + "          WHERE POP.X_USUARIO = :xUsuario        "
      + "          AND PTE.X_EMPLEADO = POP.X_EMPLEADO             "
      + "          AND PTE.X_CENTRO = :idCentro            "
      + "          AND PTE.F_TOMAPOS = POP.F_TOMAPOS             "
      + "          AND POP.x_PERFIL IN (161,11207,13207)  "
      + "          AND :vista not in ('Profesorado')  "
      + "          AND PTE.F_TOMAPOSREA <= SYSDATE             "
      + "          AND (PTE.F_CESE >= SYSDATE OR PTE.F_CESE IS NULL)   "
      + "          UNION "
      + "          SELECT PTE.X_EMPLEADO AS x_empleado,             "
      + "                 TO_CHAR(PTE.F_TOMAPOS,'dd/MM/yyyy') AS f_tomapos,             "
      + "                 PTE.X_CENTRO AS x_centro,             "
      + "                 TLF_ANNOACTUAL AS c_anno,             "
      + "                 TO_CHAR(:xUsuario) AS x_usuario,                "
      + "                 '2' x_perfil,       "
      + "                 'P' codigo_perfil,       "
      + "               NVL((SELECT DISTINCT        "
      + "                      EMP.NOMBRE || ' ' || EMP.APELLIDO1 || ' ' || EMP.APELLIDO2       "
      + "                      FROM TLPTOTRAEMP PTO        "
      + "                      INNER JOIN TLEMPLEADOS EMP ON EMP.X_EMPLEADO  = PTO.X_EMPLEADO        "
      + "                      INNER JOIN TLPUESTOS PUT ON PTO.X_PUESTO = PUT.X_PUESTO         "
      + "                      INNER JOIN FCT_TUTORFCTDUAL TUT ON TUT.X_EMPLEADO = PTO.X_EMPLEADO_SUSTITUYE        "
      + "                      INNER JOIN FCT_TUTORFCTDUAL TUT1 ON TUT1.X_EMPLEADO = PTO.X_EMPLEADO        "
      + "                      WHERE PTO.F_TOMAPOS = PTE.F_TOMAPOS       "
      + "                      AND PTO.X_EMPLEADO = PTE.X_EMPLEADO        "
      + "                      AND ROWNUM <= 1),NULL) sustituto,       "      
      + "              (select ID_TUTORFCTDUAL  "
      + "              from  FCT_TUTORFCTDUAL TUT, " 
      + "                   TLPTOTRAEMP PTO1  "
      + "               WHERE TUT.X_EMPLEADO = PTE.X_EMPLEADO " 
      + "               AND PTO1.X_EMPLEADO = TUT.X_EMPLEADO   "
      + "               AND TUT.F_TOMAPOS = PTO1.F_TOMAPOS  "
      + "               AND PTO1.X_CENTRO = PTE.X_CENTRO " 
      + "               AND sysdate between tut.f_initutoria and f_baja "
      + "               AND ROWNUM <= 1 )  idtutorfctdual, "              
      + "             NVL((SELECT DISTINCT          "      
      + "                    TUT.ID_TUTORFCTDUAL      "         
      + "                    FROM TLPTOTRAEMP PTO       "         
      + "                    INNER JOIN TLEMPLEADOS EMP ON EMP.X_EMPLEADO  = PTO.X_EMPLEADO "               
      + "                    INNER JOIN TLPUESTOS PUT ON PTO.X_PUESTO = PUT.X_PUESTO          "       
      + "                    INNER JOIN FCT_TUTORFCTDUAL TUT ON TUT.X_EMPLEADO = PTO.X_EMPLEADO_SUSTITUYE "                                  
      + "                    WHERE PTO.F_TOMAPOS = PTE.F_TOMAPOS               "
      + "                    AND PTO.X_EMPLEADO = PTE.X_EMPLEADO                "
      + "                    AND ROWNUM <= 1),-1)   as idtutorfctdualsus, "      
      + "               NULL s_modulo,       "
      + "               NULL x_ident,       "
      + "               '0' esDirector, "
      + "               CASE "
      + "               WHEN (SELECT count(distinct x_empleado) " 
      + "                     FROM  TLMIEDEPART dep  "
      + "                     WHERE dep.X_EMPLEADO = PTE.X_EMPLEADO " 
      + "                     AND dep.F_TOMAPOS = PTE.F_TOMAPOS  "
      + "                     AND dep.L_JEFE = 'S'  "
      + "                     AND (DEP.F_FIN > TRUNC(SYSDATE) OR DEP.F_FIN IS NULL)) >0 THEN '1' "
      + "               ELSE '0' END esJefeDep, "
      + "               CASE  "
      + "               WHEN  (SELECT COUNT(DISTINCT  1)  "
      + "                      FROM TLCARGOSEMP car " 
      + "                      WHERE car.X_EMPLEADO = PTE.X_EMPLEADO " 
      + "                      AND car.X_EMPLEADO IS NOT NULL  "
      + "                      AND car.F_TOMAPOS = PTE.F_TOMAPOS " 
      + "                      AND (CAR.F_CESE  > TRUNC(SYSDATE) OR CAR.F_CESE  IS NULL) " 
      + "                      AND car.C_CARGO = 'XCC') >0 THEN '1' "
      + "               ELSE '0' end esCoorCiclo, "
      + "               (select c_provincia from tldatoscen where x_centro = :idCentro and ROWNUM <= 1) c_provincia, "
      + "                2 orden "
      + "          FROM TLPTOTRAEMP PTE, TLPUEORIPER POP       "
      + "          WHERE POP.X_USUARIO = :xUsuario        "
      + "          AND PTE.X_EMPLEADO = POP.X_EMPLEADO             "
      + "          AND PTE.X_CENTRO = :idCentro            "
      + "          AND PTE.F_TOMAPOS = POP.F_TOMAPOS             "
      + "          AND POP.x_PERFIL = 2       "
      + "          AND PTE.F_TOMAPOSREA <= SYSDATE             "
      + "          AND (PTE.F_CESE >= SYSDATE OR PTE.F_CESE IS NULL)   "
      + "          AND :vista in ('Profesorado')           "
      + "           UNION         "
      + "           SELECT PTE.X_EMPLEADO AS x_empleado,                    "
      + "           TO_CHAR(PTE.F_TOMAPOS,'dd/MM/yyyy') AS f_tomapos,                    "
      + "           PTE.X_CENTRO AS x_centro,                    "
      + "           TLF_ANNOACTUAL AS c_anno,                   "
      + "           TO_CHAR(:xUsuarioComunica) AS x_usuario,                      "
      + "           (select TO_CHAR(x_perfil) from DELPHOS_SEGEDU.tlperfiles where x_perfil = POP.x_perfil ) AS x_perfil,              "
      + "           (select c_codigo from DELPHOS_SEGEDU.tlperfiles where x_perfil = POP.x_perfil ) AS codigo_perfil,              "
      + "           NULL sustituto,              "
      + "           NULL idtutorfctdual,              "
      + "           NULL idtutorfctdualsus,                     "
      + "           '1' s_modulo,              "
      + "           '1' x_ident,              "
      + "           '0' esDirector, "
      + "           '0' esJefeDep, "
      + "           '0' esCoorCiclo, "
      + "           (select c_provincia from tldatoscen where x_centro = :idCentro and ROWNUM <= 1) c_provincia, "
      + "           3 orden "
      + "           FROM DELPHOS_SEGEDU.TLPTOTRAEMP PTE, DELPHOS_SEGEDU.TLPUEORIPER POP              "
      + "           WHERE POP.X_USUARIO = :xUsuarioComunica             "
      + "           AND PTE.X_EMPLEADO = POP.X_EMPLEADO                   "
      + "           AND PTE.X_CENTRO = :idCentro                   "
      + "           AND PTE.F_TOMAPOS = POP.F_TOMAPOS                    "
      + "           AND POP.x_PERFIL = 5000             "
      + "           AND PTE.F_TOMAPOSREA <= SYSDATE        "
      + "           AND :vista not in ('Direccion','Profesorado')       "
      + "           AND (PTE.F_CESE >= SYSDATE OR PTE.F_CESE IS NULL)"
      + "           UNION            "
      + "           SELECT PTE.X_EMPLEADO AS x_empleado,                       "
      + "           TO_CHAR(PTE.F_TOMAPOS,'dd/MM/yyyy') AS f_tomapos,                       "
      + "           PTE.X_CENTRO AS x_centro,                       "
      + "           TLF_ANNOACTUAL AS c_anno,                      "
      + "           TO_CHAR(USU.x_usuario) AS x_usuario,                         "
      + "           (select TO_CHAR(x_perfil) from DELPHOS_SEGEDU.tlperfiles where x_perfil = POP.x_perfil ) AS x_perfil,                 "
      + "           (select c_codigo from DELPHOS_SEGEDU.tlperfiles where x_perfil = POP.x_perfil ) AS codigo_perfil,                 "
      + "           NULL sustituto,                 "
      + "           NULL idtutorfctdual,                 "
      + "           NULL idtutorfctdualsus,                        "
      + "           '1' s_modulo,                 "
      + "           '1' x_ident,                 "
      + "           '0' esDirector, "
      + "           '0' esJefeDep, "
      + "           '0' esCoorCiclo, "
      + "           (select c_provincia from tldatoscen where x_centro = :idCentro and ROWNUM <= 1) c_provincia, "
      + "           3 orden    "
      + "           FROM DELPHOS_SEGEDU.TLPTOTRAEMP PTE, DELPHOS_SEGEDU.TLPUEORIPER POP, delphos_modacc.usuarios usu, delphos_modacc.documentaciones doc,  "
      + "           DELPHOS_SEGEDU.TLEMPLEADOS EMP, DELPHOS_SEGEDU.TLUSUARIOS USU                   "
      + "           WHERE POP.X_USUARIO = USU.x_usuario                "
      + "           AND PTE.X_EMPLEADO = POP.X_EMPLEADO                      "
      + "           AND PTE.X_CENTRO = :idCentro                      "
      + "           AND PTE.F_TOMAPOS = POP.F_TOMAPOS                       "
      + "           AND POP.x_PERFIL = 5000                "
      + "           AND PTE.F_TOMAPOSREA <= SYSDATE    "
      + "           AND (:xUsuarioComunica is null OR :xUsuarioComunica = -1)  "
      + "           and USU.x_empleado = EMP.x_empleado  "
      + "           AND usu.oid = :oid "
      + "           and doc.oid_persona =  usu.oid_persona "
      + "           and emp.c_numide = doc.t_identificacion  "
      + "           AND :vista not in ('Direccion','Profesorado')          "
      + "           AND (PTE.F_CESE >= SYSDATE OR PTE.F_CESE IS NULL)            "
      + "           order by orden)          "
      + "           WHERE ROWNUM <= 1 ", nativeQuery = true)
 List<ParamsFCTProjection> getParamsFCT(String vista, Long idCentro, Long xUsuario, Long xUsuarioComunica, Long oid );
    
    
    @Query(value = " select usc.x_usuario from delphos_modacc.usuarios usu,  "
    		+ "              delphos_modacc.documentaciones doc, "
    		+ "              delphos_segedu.tlempleados emp, "
    		+ "              delphos_segedu.tlusuarios usc "
    		+ "where usu.oid = :oid "
    		+ "and doc.oid_persona = usu.oid_persona "
    		+ "and emp.c_numide = doc.t_identificacion "
    		+ "and usc.x_empleado  = emp.x_empleado "
    		+ "and rownum <=1 ", nativeQuery = true)  
    Long getXusuarioComunicaByOid(Long oid);
    
    @Query(value = " select emp.x_empleado from delphos_modacc.usuarios usu,  "
    		+ "              delphos_modacc.documentaciones doc, "
    		+ "              delphos_segedu.tlempleados emp, "
    		+ "              delphos_segedu.tlusuarios usc "
    		+ "where usu.oid = :oid "
    		+ "and doc.oid_persona = usu.oid_persona "
    		+ "and emp.c_numide = doc.t_identificacion "
    		+ "and usc.x_empleado  = emp.x_empleado "
    		+ "and rownum <=1 ", nativeQuery = true)  
    Long getXempleadoComunicaByOid(Long oid);
 

}
