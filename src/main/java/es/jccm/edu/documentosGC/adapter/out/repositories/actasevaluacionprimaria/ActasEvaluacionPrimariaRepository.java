package es.jccm.edu.documentosGC.adapter.out.repositories.actasevaluacionprimaria;

import java.util.List;
import org.springframework.data.jpa.repository.Query;
import es.jccm.edu.documentosGC.application.domain.actasevaluacionprimaria.projection.DirectoresActaPrivadoProjection;
import es.jccm.edu.documentosGC.application.domain.actasevaluacionprimaria.projection.ProfesorActaEvaluacionPrimariaProjection;
import es.jccm.edu.documentosGC.application.domain.documentosGC.entities.DocumentosGC;
import es.jccm.edu.documentosGC.application.domain.documentosGC.entities.QDocumentosGC;
import es.jccm.edu.shared.adapter.out.repositories.AbstractRepository;

public interface ActasEvaluacionPrimariaRepository extends AbstractRepository<DocumentosGC, Long, QDocumentosGC>  {
 
 @Query(value =" select pte.x_empleado  as id, "
   + "       tlf_nombreempleado(pte.x_empleado) as nombre, "
   + "       null as nombredepartamento "
   + "  from tlptotraemp pte, "
   + "       tltipper tp, "
   + "       tlhorariosr hr, tlactividades act "
   + " where pte.x_centro = :idCentro "
   + "   and hr.x_empleado = pte.x_empleado "
   + "    and hr.f_tomapos = pte.f_tomapos "
   + "   and hr.c_anno = :cAnno "
   + "   and act.x_actividad = hr.x_actividad "   
   + "   and tlf_intersecper(pte.f_tomaposrea, pte.f_cese, NVL(TO_DATE(:fSesion, 'yyyy-MM-dd'),NVL(TO_DATE(:f_fecfinconomc, 'dd-MM-yyyy'), TO_DATE(:f_fecfincon, 'dd-MM-yyyy'))),  "
            + "                                                     NVL (TO_DATE(:fSesion, 'yyyy-MM-dd'),NVL(TO_DATE(:f_fecfinconomc, 'dd-MM-yyyy'), TO_DATE(:f_fecfincon, 'dd-MM-yyyy')))) = 1 "
   + "   and tp.x_tipoper = pte.x_tipoper "
   + "   and tp.l_firmaacta = 'S' "
   + "union "
   + "select m.x_empleado as id, "
   + "       tlf_nombreempleado(m.x_empleado) as nombre, "
   + "       decode(dtc.x_departamento, null, 'J. Dpto. ' || ddg.d_departamento, 'J. Dpto. ' || dep.d_departamento) as nombredepartamento "
   + "  from tlmiedepart m, "
   + "       tlptotraemp pte, "
   + "       tltipper tp, "
   + "       tldepartcen dtc, "
   + "       tldepdengen ddg, "
   + "       tldepartamentos dep "
   + " where m.l_jefe = 'S' "
   + "   and m.l_activo = 'S' "
   + "   and pte.x_empleado = m.x_empleado "
   + "   and pte.f_tomapos = m.f_tomapos "   
   + " and tlf_intersecper(m.f_inicio, m.f_fin, NVL(TO_DATE(:fSesion, 'yyyy-MM-dd'),NVL(TO_DATE(:f_fecfinconomc, 'dd-MM-yyyy'), TO_DATE(:f_fecfincon, 'dd-MM-yyyy'))),  "
            + "                                          NVL (TO_DATE(:fSesion, 'yyyy-MM-dd'),NVL(TO_DATE(:f_fecfinconomc, 'dd-MM-yyyy'), TO_DATE(:f_fecfincon, 'dd-MM-yyyy')))) = 1 "
   + "   and tp.x_tipoper = pte.x_tipoper "
   + "   and tp.l_firmaacta = 'S' "
   + "   and dtc.x_departcen = m.x_departcen "
   + "   and dtc.x_centro = :idCentro "
   + "   and dtc.c_anno = :cAnno "
   + "   and ddg.x_depdengen (+) = dtc.x_depdengen "
   + "   and dep.x_departamento (+) = dtc.x_departamento "
   + "minus "
   + "select distinct pte.x_empleado as id, "
   + "       tlf_nombreempleado(pte.x_empleado) as nombre, "
   + "       null as nombredepartamento "
   + "  from tlofematrcen omc, "
   + "       tlptotraemp pte, "
   + "      tltipper tp, "
   + "       tlhorariosr hr, "
   + "       tlactividades act, "
   + "       tluniafetrahor uath "
   + " where omc.x_ofertamatrig = :idOfertamatrig "
   + "   and omc.x_centro = :idCentro "
   + "   and pte.x_centro = omc.x_centro "
   + "   and hr.x_empleado = pte.x_empleado "
   + "   and hr.f_tomapos = pte.f_tomapos "
   + "   and hr.c_anno = :cAnno "
   + "   and act.x_actividad = hr.x_actividad "
   + "   and act.l_docdirecta = 'S' "
   + "   and act.t_abreviatura = 'LE' "
   + "   and uath.x_horariore = hr.x_horariore "
   + "   and uath.x_ofertamatric = omc.x_ofertamatric "
   + "   and uath.x_unidad = :idUnidad "
   + "   and tlf_intersecper(pte.f_tomaposrea, pte.f_cese, NVL(TO_DATE(:fSesion, 'yyyy-MM-dd'),NVL(TO_DATE(:f_fecfinconomc, 'dd-MM-yyyy'), TO_DATE(:f_fecfincon, 'dd-MM-yyyy'))),  "
            + "                                                     NVL(TO_DATE(:fSesion, 'yyyy-MM-dd'),NVL(TO_DATE(:f_fecfinconomc, 'dd-MM-yyyy'), TO_DATE(:f_fecfincon, 'dd-MM-yyyy')))) = 1 "
   + "   and not exists (select 1 "
   + "                   from tlptotraemp p "
   + "                    where p.x_centro = pte.x_centro "
   + "                   and p.x_empleado_sustituye = pte.x_empleado "
   + "                   and p.f_tomapos_sustituye = pte.f_tomapos "
   + "                   and tlf_intersecper(p.f_tomaposrea, p.f_cese, NVL(TO_DATE(:fSesion, 'yyyy-MM-dd'),NVL(TO_DATE(:f_fecfinconomc, 'dd-MM-yyyy'), TO_DATE(:f_fecfincon, 'dd-MM-yyyy'))), " 
            + "                                                                 NVL(TO_DATE(:fSesion, 'yyyy-MM-dd'),NVL(TO_DATE(:f_fecfinconomc, 'dd-MM-yyyy'), TO_DATE(:f_fecfincon, 'dd-MM-yyyy')))) = 1)  "
   + "   and tp.x_tipoper = pte.x_tipoper "
   + "   and tp.l_firmaacta = 'S' "
   + " order by nombre", nativeQuery = true)
 List<ProfesorActaEvaluacionPrimariaProjection> getProfesoresCandidatosActaEvaluacion(Long idCentro, Integer cAnno, String fSesion,
   Long idOfertamatrig, Long idUnidad, String f_fecfinconomc, String f_fecfincon);

 @Query(value ="select distinct pte.x_empleado as id, "
   + "       tlf_nombreempleado(pte.x_empleado) as nombre, "
   + "       null as nombredepartamento "
   + "  from tlofematrcen omc, "
   + "       tlptotraemp pte, "
   + "      tltipper tp, "
   + "       tlhorariosr hr, "
   + "       tlactividades act, "
   + "       tluniafetrahor uath "
   + " where omc.x_ofertamatrig = :idOfertamatrig "
   + "   and omc.x_centro = :idCentro "
   + "   and pte.x_centro = omc.x_centro "
   + "   and hr.x_empleado = pte.x_empleado "
   + "   and hr.f_tomapos = pte.f_tomapos "
   + "   and hr.c_anno = :cAnno "
   + "   and act.x_actividad = hr.x_actividad "
   + "   and act.l_docdirecta = 'S' "
   + "   and act.t_abreviatura = 'LE' "
   + "   and uath.x_horariore = hr.x_horariore "
   + "   and uath.x_ofertamatric = omc.x_ofertamatric "
   + "   and uath.x_unidad = :idUnidad "
            + "   and tlf_intersecper(pte.f_tomaposrea, pte.f_cese, NVL(TO_DATE(:fSesion, 'yyyy-MM-dd'),NVL(TO_DATE(:f_fecfinconomc, 'dd-MM-yyyy'), TO_DATE(:f_fecfincon, 'dd-MM-yyyy'))),  "
            + "                                                     NVL(TO_DATE(:fSesion, 'yyyy-MM-dd'),NVL(TO_DATE(:f_fecfinconomc, 'dd-MM-yyyy'), TO_DATE(:f_fecfincon, 'dd-MM-yyyy')))) = 1  " 
   + "   and not exists (select 1 "
   + "                   from tlptotraemp p "
   + "                   where p.x_centro = pte.x_centro "
   + "                   and p.x_empleado_sustituye = pte.x_empleado "
   + "                   and p.f_tomapos_sustituye = pte.f_tomapos "
   + "                   and tlf_intersecper(p.f_tomaposrea, p.f_cese, NVL(TO_DATE(:fSesion, 'yyyy-MM-dd'),NVL(TO_DATE(:f_fecfinconomc, 'dd-MM-yyyy'), TO_DATE(:f_fecfincon, 'dd-MM-yyyy'))),  "
   + "                                                                 NVL(TO_DATE(:fSesion, 'yyyy-MM-dd'),NVL(TO_DATE(:f_fecfinconomc, 'dd-MM-yyyy'), TO_DATE(:f_fecfincon, 'dd-MM-yyyy')))) = 1)  "
   + "   and tp.x_tipoper = pte.x_tipoper "
   + "   and tp.l_firmaacta = 'S' "
   + " order by nombre", nativeQuery = true)
 List<ProfesorActaEvaluacionPrimariaProjection> getProfesoresSeleccionadosActaEvaluacion(Long idCentro, Integer cAnno, String fSesion,
   Long idOfertamatrig, Long idUnidad, String f_fecfinconomc, String f_fecfincon); 

 
 @Query(value =" select ce.x_empleado AS id, "
   + " tlf_nombreempleado(ce.x_empleado) AS nombre "
   + " FROM tlptotraemp pto, "
   + " tltipper tp, "
   + " tlcargosemp ce,"
   + " tlcentros cen, "
   + " tlcargos car, "
   + " tlconvcentros convc "
   + " where convc.x_convcentro = :idConvCentro"
   + " and pto.x_centro = convc.x_centro "
   + " and tlf_intersecper(pto.f_tomaposrea, pto.f_cese, convc.f_fecinicon, convc.f_fecfincon) = 1 "
   + " and ce.x_empleado = pto.x_empleado "
   + " and ce.f_tomapos = pto.f_tomapos "
   + " and tlf_intersecper(ce.f_tompos, ce.f_cese, convc.f_fecinicon, convc.f_fecfincon) = 1 "
   + " and cen.x_centro = pto.x_centro "
   + " and (   (cen.x_tipo = 1 and ce.c_cargo = 'XDI') "
   + "  or (cen.x_tipo = 2 and ce.c_cargo in ('XDI', 'XDP')) "
   + "  ) "
   + " and car.c_cargo = ce.c_cargo "
   + " and not exists (select 1 "
   + "  from tlptotraemp p  "
   + "  where p.x_centro = pto.x_centro "
   + "  and p.x_empleado_sustituye = pto.x_empleado "
   + "  and p.f_tomapos_sustituye = pto.f_tomapos "
   + "  and tlf_intersecper(p.f_tomaposrea, p.f_cese, convc.f_fecinicon, convc.f_fecfincon) = 1) "
   + " and tp.x_tipoper = pto.x_tipoper "
   + " and tp.l_firmaacta = 'S' ", nativeQuery = true)
 List<DirectoresActaPrivadoProjection> getListadoDirectoresPrivado(Long idConvCentro);

}
